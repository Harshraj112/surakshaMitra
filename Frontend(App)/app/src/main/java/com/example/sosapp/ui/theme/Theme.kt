@Composable
fun SOSAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false, // Disabled for consistent emergency branding
    highContrast: Boolean = false, // Option for accessibility
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        highContrast -> {
            if (darkTheme) emergencyHighContrastDarkScheme else emergencyHighContrastLightScheme
        }

        darkTheme -> emergencyDarkScheme
        else -> emergencyLightScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}